package br.com.codinglab.myapp.model;

public class Contact {

    private int id;
    private String username;
    private String name;
    private String photoUrl;

    public int getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getName() {
        return name;
    }

    public String getPhotoUrl() {
        return photoUrl;
    }
}
